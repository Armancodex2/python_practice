"Take two numbers as input from the user. print their sum, difference, product and remainder."

num1 = int(input("Enter the first number = "))
num2 = int(input("Enter the second number = "))

sum = num1 + num2
diff = num1 - num2
product = num1 * num2
remainder = num1 % num2


print(f"Sum = {sum}")
print(f"Difference = {diff}")
print(f"Product = {product}")
print(f"Remainder = {remainder}")
